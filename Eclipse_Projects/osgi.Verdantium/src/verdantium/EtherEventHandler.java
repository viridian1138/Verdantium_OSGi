/**
* Any class that handles object-level EtherEvents must implement this interface.
* EtherEvents are sent to an object using the processObjEtherEvent() method.
* 
* @author Thorn Green
*/
public interface EtherEventHandler {
	
	/**
	* Handles an EtherEvent request.  The event to be handled is contained in <code>in</code>.
	* Once the object handles the event, it is to return the result.  
	* The <code>refcon</code> field is for any aux. information
	* that the object may need to receive.
	* @param in The event to handle.
	* @param refcon A reference to context data that the generating code can associate with the event.  See various references to "refcon" in MacOS programming.
	* @return The result of handling the event, or null if there is no result.
	*/
	public Object processObjEtherEvent(EtherEvent in, Object refcon)
		throws Throwable;

	
}

