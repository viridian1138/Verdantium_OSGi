/**
* Provides a version of the Rectangle2D class that can be saved to serial storage.
* 
* @author Thorn Green
*/
public class SerRectangle2D extends Rectangle2D.Double implements Externalizable {

	/**
	* Constructs the Rectangle.
	*/
	public SerRectangle2D() {
		super();
	}

	/**
	* Constructs the rectangle with a position, width, and height.
	* @param x The rectangle X-Axis position.
	* @param y The rectangle Y-Axis position.
	* @param w The rectangle width.
	* @param h The rectangle height.
	*/
	public SerRectangle2D(double x, double y, double w, double h) {
		super(x, y, w, h);
	}

	/**
	* Reads the rectangle from serial storage.
	*/
	public void readExternal(ObjectInput in) throws IOException, ClassNotFoundException {
		VersionBuffer myv = (VersionBuffer) (in.readObject());

		height = myv.getDouble("height");
		width = myv.getDouble("width");
		x = myv.getDouble("x");
		y = myv.getDouble("y");
	}

	/**
	* Writes the rectangle to serial storage.
	* @serialData TBD.
	*/
	public void writeExternal(ObjectOutput out) throws IOException {
		VersionBuffer myv = new VersionBuffer(VersionBuffer.WRITE);

		myv.setDouble("height", height);
		myv.setDouble("width", width);
		myv.setDouble("x", x);
		myv.setDouble("y", y);

		out.writeObject(myv);
	}

	
}

