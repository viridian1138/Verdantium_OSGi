/**
* A standard interface for getting and altering the document "page"
* size of a component that supports full-page scrolling in two dimensions.
* <P>
* @author Thorn Green
*/
public interface PageSizeHandler
	extends EtherEventHandler, PropertyChangeSource {
	
	/**
	* Gets the size of the document page.
	* @return The size of the document page.
	*/
	public Dimension getPageSize();
}
